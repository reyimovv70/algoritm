# Ввод количества элементов
while True:
    n = int(input("Введите количество элементов массива (n > 0): "))
    if n > 0:
        break
    print("Количество элементов должно быть больше 0. Попробуйте ещё раз.")

# Ввод элементов массива
arr = []
for i in range(n):
    num = int(input(f"Введите элемент {i+1}: "))
    arr.append(num)

# Вывод массива
print("Введённый массив:")
for i in range(n):
    print(arr[i], end=" ")
print()  # переход на новую строку